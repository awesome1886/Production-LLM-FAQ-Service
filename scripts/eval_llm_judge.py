"""Calibration harness for the LLM-judge hallucination scanner.

Runs ``src.guardrails.llm_judge.check_hallucination`` against the same
30 positives + 30 negatives that calibrated the NLI scanner in PR #33,
reports FPR/TPR/Youden's J directly. No threshold sweep — the judge
returns a discrete verdict.

The script patches ``log_request`` to a no-op so calibration runs don't
pollute ``data/cost_log.jsonl``. Real /query traffic still gets logged.

Usage:

    make eval-llm-judge
"""

import csv
import json
import sys
from pathlib import Path
from unittest.mock import patch

from src.evaluation import build_eval_dataset, load_golden_set
from src.guardrails.llm_judge import check_hallucination
from src.models import Source

POSITIVES_PATH = Path("data/golden_test_set.csv")
NEGATIVES_PATH = Path("data/negative_test_set.csv")

# Reference baseline for the comparison table — pulled from
# docs/verifications/2026-04-29-factuality-threshold-tuning.md.
NLI_BASELINE = ("NLI@0.05", 0.40, 0.93)


def _to_sources(chunks: list[str]) -> list[Source]:
    return [Source(doc_id=f"p{i}", chunk_text=c, similarity_score=0.9) for i, c in enumerate(chunks)]


def _load_negatives() -> list[dict]:
    if not NEGATIVES_PATH.exists():
        sys.exit(
            f"ERROR: {NEGATIVES_PATH} is missing. The negative cohort is "
            "generated by a one-off REPL session against gpt-4o-mini; see "
            "docs/verifications/2026-04-29-factuality-threshold-tuning.md "
            "for the prompt template."
        )
    with open(NEGATIVES_PATH, newline="") as f:
        return list(csv.DictReader(f))


def main() -> int:
    print("Loading positive cohort (running RAG pipeline against golden set)…")
    golden = load_golden_set(POSITIVES_PATH)
    pos_dataset = build_eval_dataset(golden)
    positives = list(zip(pos_dataset["question"], pos_dataset["answer"], pos_dataset["contexts"]))

    print("Loading negative cohort…")
    negatives = [
        (r["question"], r["hallucinated_answer"], json.loads(r["contexts"]), r["negative_kind"])
        for r in _load_negatives()
    ]

    print(f"Positives: {len(positives)}    Negatives: {len(negatives)}")
    print("Running LLM judge…  (~1-2s per call, ~60 calls)")

    # Suppress cost-log writes during calibration runs.
    with patch("src.guardrails.llm_judge.output_guards.log_request"):
        pos_results = [
            (q, check_hallucination(a, _to_sources(c))) for q, a, c in positives
        ]
        neg_results = [
            (q, kind, check_hallucination(a, _to_sources(c)))
            for q, a, c, kind in negatives
        ]

    pos_blocked = [(q, r) for q, r in pos_results if r is not None]
    neg_blocked = [(q, kind, r) for q, kind, r in neg_results if r is not None]

    fpr = len(pos_blocked) / len(pos_results)
    tpr = len(neg_blocked) / len(neg_results)
    j = tpr - fpr

    print("\n## Comparison\n")
    print("| scanner   | FPR  | TPR  | Youden's J |")
    print("|-----------|------|------|------------|")
    print(f"| {NLI_BASELINE[0]:<9} | {NLI_BASELINE[1]:.2f} | {NLI_BASELINE[2]:.2f} | {NLI_BASELINE[2] - NLI_BASELINE[1]:+.2f}      |")
    print(f"| LLM judge | {fpr:.2f} | {tpr:.2f} | {j:+.2f}      |")

    # Outliers
    print("\n## Outliers\n")
    if pos_blocked:
        print(f"**Positives blocked by judge ({len(pos_blocked)}/{len(pos_results)}):**")
        for q, r in pos_blocked:
            print(f"  - {q}")
            print(f"    reason: {r}")
    else:
        print("No grounded answers were blocked. ✓")
    print()
    if any(r is None for _, _, r in neg_results):
        passed = [(q, kind) for q, kind, r in neg_results if r is None]
        print(f"**Negatives that passed through ({len(passed)}/{len(neg_results)}):**")
        for q, kind in passed:
            print(f"  - [{kind}] {q}")
    else:
        print("All hallucinations were caught. ✓")

    # Acceptance gate (from the plan)
    print()
    if fpr <= 0.10 and tpr >= 0.80:
        print(f"**Acceptance gate PASSED:** FPR={fpr:.2f} ≤ 0.10 and TPR={tpr:.2f} ≥ 0.80 ✓")
        return 0
    else:
        print(f"**Acceptance gate FAILED:** FPR={fpr:.2f} (target ≤ 0.10), TPR={tpr:.2f} (target ≥ 0.80)")
        print("Iterate on the prompt at prompts/judge.j2 before shipping.")
        return 1


if __name__ == "__main__":
    sys.exit(main())
